import math

# ==============================================================================
# ACL RISK SCORE V4 (สอดคล้องหลักการทางฟิสิกส์และความเร่งเชิงมุมตามเล่มโครงงาน)
# ==============================================================================

def acl_risk_score(
    left_knee,
    right_knee,
    asymmetry,
    left_valgus,
    right_valgus,
    velocity,
    acceleration,
    age,
    gender,
    landing=False
):
    score = 0
    avg_angle = (left_knee + right_knee) / 2
    valgus = max(left_valgus, right_valgus)
    
    # สัญลักษณ์ตามเล่มรายงาน: ω (ความเร็วเชิงมุม) และ α (ความเร่งเชิงมุม)
    omega = abs(velocity)
    alpha = abs(acceleration)

    # -------------------------------------------------------------
    # 1. Knee Flexion (มุมการงอเข่า)
    # -------------------------------------------------------------
    # ยืนตรงหรือเข่าเหยียดตึงมากเกินไป (>165°) ACL จะรับโหลดแรงเต็มๆ เสี่ยงสูงสุด
    if avg_angle > 165:
        score += 25
    elif avg_angle > 150:
        score += 15
    elif avg_angle > 130:
        score += 8
    else:
        score += 2   # การงอเข่าเยอะ (Safe flexion) ช่วยซับแรงกระแทก

    # -------------------------------------------------------------
    # 2. Dynamic Knee Valgus (การบิดเข้าในของข้อเข่าขณะเคลื่อนไหว)
    # -------------------------------------------------------------
    if valgus > 30:
        score += 35  
    elif valgus > 22:
        score += 25
    elif valgus > 15:
        score += 12
    elif valgus > 8:
        score += 5

    # -------------------------------------------------------------
    # 3. Asymmetry (ความไม่สมดุลของร่างกายซ้าย-ขวา)
    # -------------------------------------------------------------
    if asymmetry > 25:
        score += 15
    elif asymmetry > 12:
        score += 7

    # -------------------------------------------------------------
    # 4. Kinematics (ความเร็วเชิงมุม ω และ ความเร่งเชิงมุม α)
    # -------------------------------------------------------------
    # ตามทฤษฎีบท 2.1.4: ค่าความเร่งที่สูงมากในช่วงเวลาสั้นๆ บ่งบอกถึงแรงกระแทกและการชะลอตัววิกฤต
    if omega > 180 or alpha > 1800:
        score += 20
    elif omega > 100 or alpha > 1000:
        score += 12
    elif omega > 50 or alpha > 400:
        score += 6

    # -------------------------------------------------------------
    # 5. BIOMECHANICAL COMBINATION MULTIPLIER (ตัวคูณกลไกความเสี่ยงร่วม)
    # -------------------------------------------------------------
    multiplier = 1.0

    # จังหวะอันตรายสูงสุด: เข่าบิดเข้าใน (Valgus) + เข่าตึงเหยียดตรง (Extension) + มีความเร่งกระแทกสูง (α)
    # นี่คือกลไกทางฟิสิกส์ที่ทำให้เอ็น ACL ฉีกขาดโดยตรง (ACL Rupture Mechanism)
    if valgus > 18 and avg_angle > 155:
        if alpha > 800 or omega > 120:
            multiplier += 1.0  # เพิ่มความแรงของคะแนนเป็น 2 เท่า (ทวีคูณความเสี่ยงทันที)
        else:
            multiplier += 0.5

    # ความไม่สมดุลร่วมกับแรงหมุนบิดเฉียบพลัน
    if asymmetry > 20 and valgus > 20:
        multiplier += 0.3

    # คำนวณคะแนนรวมสุทธิหลังจากคูณปัจจัยทางชีวกลศาสตร์
    score = score * multiplier

    # -------------------------------------------------------------
    # 6. Athlete Profile (ปัจจัยเสริมทางสรีระวิทยา)
    # -------------------------------------------------------------
    if gender == "Female":
        score += 4  # ผู้หญิงมีอัตราการบาดเจ็บ ACL สูงกว่าตามสถิติ
    if age < 20:
        score += 2

    # -------------------------------------------------------------
    # Safe Zone Filter (ตัวกรองตัดสัญญาณเพี้ยน)
    # -------------------------------------------------------------
    # หากขยับตัวช้ามากๆ (นั่งชันเข่าเฉยๆ หรือแยกขานิ่งๆ) ω และ α ต่ำ ไม่ถือว่าเป็นกลไกการบาดเจ็บ
    if omega < 30 and alpha < 300 and not landing:
        # จำกัดคะแนนไม่ให้เกินระดับพื้นฐานตามรูปทรงข้อต่อ
        score = min(score, 25)

    # ควบคุมให้อยู่ในช่วงเปอร์เซ็นต์ 0 - 100%
    score = max(0, min(score, 100))

    return round(score)